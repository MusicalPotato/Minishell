#include "libft.h"

void	ft_sort(int *tab, int size)
{
	int	a;

	a = -1;
	while (++a < size)
		if (tab[a] < tab[a + 1])
		{
			ft_swap(&tab[a], &tab[a + 1]);
			a = -1;
		}
	return ;
}
